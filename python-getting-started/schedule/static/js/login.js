function login() {
  $.post( "/ajax/login/", JSON.stringify({'email': $('#email').val(), 'password': $('#password').val()}), function(data, status) {
    console.log(data);
    if (data.success) {
      window.location.href = '/groups';
    } else { // failure
      $('#errorMsg').html(data.failure)
    }
  });
}

